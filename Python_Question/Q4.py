''' Write a Python program to get the 4th element from front and 4th element from last of a 
tuple. '''

# Define the tuple
my_tuple = ('a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j')

# Access the 4th element from the front (index 3)
fourth_from_front = my_tuple[3]

# Access the 4th element from the end (index -4)
fourth_from_end = my_tuple[-4]

# Print the results
print(f"4th element from the front: {fourth_from_front}")
print(f"4th element from the end: {fourth_from_end}")
