''' Write a Python program to unpack a tuple in several variables. '''

# Define a tuple
my_tuple = (1, 2, 3, 4, 5)

# Unpack the tuple into variables
a, b, c, d, e = my_tuple

# Print the unpacked variables
print("Unpacked values:")
print("a =", a)
print("b =", b)
print("c =", c)
print("d =", d)
print("e =", e)
